
arr=[3,7,1,10,5,1,1]
print("this is our array: ",arr)
# append()	Adds an element at the end of the list
arr.append(200)
print("appending value 200: ",arr)
# pop()	Removes the element at the specified position
arr.pop(2)
print("removing value from 2nd index: ",arr)
# count()	Returns the number of elements with the specified value
print("counting number of elements: ",arr.count(1))
# insert()	Adds an element at the specified position
arr.insert(3,300)
print("inserting 300 at index 3: ",arr)
# reverse()	Reverses the order of the list
arr.reverse()
print("reversing the array: ",arr)
# remove()	Removes the first item with the specified value
arr.remove(300)
print("removing specific value: ",arr)
# arr.sort(reverse= True)
arr.sort()
print("sort ascending: ",arr)
arr.sort(reverse= True)
print("sort descending: ",arr)
# clear()	Removes all the elements from the list
arr.clear()
print("clearing the array: ",arr )


